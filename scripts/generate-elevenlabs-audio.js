#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const ts = require('typescript');

const ROOT = path.join(__dirname, '..');
const DECKS_DIR = path.join(ROOT, 'src/data/decks');
const AUDIO_DIR = path.join(ROOT, 'assets/audio/pronunciation');
const MANIFEST_PATH = path.join(ROOT, 'src/data/audioManifest.ts');
const DEFAULT_VOICE_ID = 'tTQzD8U9VSnJgfwC6HbY';
const MODEL_ID = 'eleven_multilingual_v2';
const OUTPUT_FORMAT = 'mp3_44100_128';

function readStringLiteral(node) {
  if (!node) return null;
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) return node.text;
  return null;
}

function propertyNameText(name) {
  if (!name) return null;
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name))
    return name.text;
  return null;
}

function findProperty(objectNode, propertyName) {
  return objectNode.properties.find((property) => {
    if (!ts.isPropertyAssignment(property)) return false;
    return propertyNameText(property.name) === propertyName;
  });
}

function extractCardsFromSource(sourceText, fileName) {
  const source = ts.createSourceFile(fileName, sourceText, ts.ScriptTarget.Latest, true);
  const cards = [];

  function visit(node) {
    if (ts.isPropertyAssignment(node) && propertyNameText(node.name) === 'cards') {
      const initializer = node.initializer;

      if (ts.isArrayLiteralExpression(initializer)) {
        initializer.elements.forEach((element) => {
          if (!ts.isObjectLiteralExpression(element)) return;

          const id = readStringLiteral(findProperty(element, 'id')?.initializer);
          const front = readStringLiteral(findProperty(element, 'front')?.initializer);

          if (id && front) cards.push({ id, front });
        });
      }
    }

    ts.forEachChild(node, visit);
  }

  visit(source);
  return cards;
}

function collectDeckCards(decksDir = DECKS_DIR) {
  const files = fs
    .readdirSync(decksDir)
    .filter((file) => file.endsWith('.ts') && file !== 'index.ts')
    .sort();

  const byId = new Map();

  files.forEach((file) => {
    const filePath = path.join(decksDir, file);
    const cards = extractCardsFromSource(fs.readFileSync(filePath, 'utf8'), file);

    cards.forEach((card) => {
      if (!byId.has(card.id)) byId.set(card.id, card);
    });
  });

  return Array.from(byId.values()).sort((a, b) => a.id.localeCompare(b.id));
}

function fileNameForCardId(cardId) {
  return `${cardId.replace(/[^a-zA-Z0-9._-]/g, '_')}.mp3`;
}

function buildGenerationPlan(cards, audioDir, options) {
  const force = Boolean(options.force);
  const limit = Number(options.limit || 0);
  const skipped = [];
  let toGenerate = cards.map((card) => ({
    ...card,
    fileName: fileNameForCardId(card.id),
  }));

  if (!force) {
    toGenerate = toGenerate.filter((card) => {
      const exists = fs.existsSync(path.join(audioDir, card.fileName));
      if (exists) skipped.push(card);
      return !exists;
    });
  }

  if (limit > 0) toGenerate = toGenerate.slice(0, limit);

  return { skipped, toGenerate };
}

function renderManifest(cards) {
  const lines = [
    '// Generated by scripts/generate-elevenlabs-audio.js.',
    '// Re-run `npm run audio:generate` after adding or changing pronunciation audio.',
  ];

  if (cards.length === 0) {
    lines.push('export const pronunciationAudio: Record<string, number> = {};', '');
    return lines.join('\n');
  }

  lines.push('export const pronunciationAudio: Record<string, number> = {');

  cards
    .slice()
    .sort((a, b) => a.id.localeCompare(b.id))
    .forEach((card) => {
      lines.push(
        `  '${card.id}': require('../../assets/audio/pronunciation/${fileNameForCardId(card.id)}'),`,
      );
    });

  lines.push('};', '');
  return lines.join('\n');
}

function requireApiKey(env) {
  const apiKey = env.ELEVENLABS_API_KEY;

  if (!apiKey) {
    throw new Error(
      'ELEVENLABS_API_KEY is required. Set it in your shell before running npm run audio:generate.',
    );
  }

  return apiKey;
}

function parseArgs(argv) {
  return argv.reduce(
    (acc, arg) => {
      if (arg === '--force') acc.force = true;
      if (arg === '--dry-run') acc.dryRun = true;
      if (arg.startsWith('--limit=')) acc.limit = Number(arg.slice('--limit='.length));
      return acc;
    },
    { force: false, limit: 0, dryRun: false },
  );
}

async function generateAudioFile({ apiKey, voiceId, card, outputPath }) {
  const url = `https://api.elevenlabs.io/v1/text-to-speech/${voiceId}?output_format=${OUTPUT_FORMAT}`;
  const response = await fetch(url, {
    method: 'POST',
    headers: {
      'xi-api-key': apiKey,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      text: card.front,
      model_id: MODEL_ID,
    }),
  });

  if (!response.ok) {
    const body = await response.text();
    throw new Error(`ElevenLabs request failed for ${card.id}: ${response.status} ${body}`);
  }

  const audio = Buffer.from(await response.arrayBuffer());
  fs.writeFileSync(outputPath, audio);
}

async function main(argv = process.argv.slice(2), env = process.env) {
  const options = parseArgs(argv);
  const apiKey = options.dryRun ? 'dry-run' : requireApiKey(env);
  const voiceId = env.ELEVENLABS_VOICE_ID || DEFAULT_VOICE_ID;

  fs.mkdirSync(AUDIO_DIR, { recursive: true });

  const cards = collectDeckCards();
  const plan = buildGenerationPlan(cards, AUDIO_DIR, options);

  console.log(`Using ElevenLabs voice ID: ${voiceId}`);
  console.log(`Cards found: ${cards.length}`);
  console.log(`Already generated: ${plan.skipped.length}`);
  console.log(`Will generate: ${plan.toGenerate.length}`);

  if (!options.dryRun) {
    const failures = [];

    for (const card of plan.toGenerate) {
      const outputPath = path.join(AUDIO_DIR, card.fileName);

      try {
        console.log(`Generating ${card.id}: ${card.front}`);
        await generateAudioFile({ apiKey, voiceId, card, outputPath });
      } catch (error) {
        failures.push({ card, error });
        console.error(error.message);
      }
    }

    if (failures.length > 0) {
      throw new Error(
        `${failures.length} audio files failed to generate. Re-run after fixing the errors.`,
      );
    }
  }

  const generatedCards = cards.filter((card) =>
    fs.existsSync(path.join(AUDIO_DIR, fileNameForCardId(card.id))),
  );

  fs.writeFileSync(MANIFEST_PATH, renderManifest(generatedCards));
  console.log(`Manifest entries: ${generatedCards.length}`);
}

if (require.main === module) {
  main().catch((error) => {
    console.error(error.message);
    process.exit(1);
  });
}

module.exports = {
  DEFAULT_VOICE_ID,
  buildGenerationPlan,
  collectDeckCards,
  extractCardsFromSource,
  fileNameForCardId,
  generateAudioFile,
  parseArgs,
  renderManifest,
  requireApiKey,
};
